package src.main.java.ClientServiceAdapter.service;

/**
 * This class represents a service in the Client-Service Adapter pattern.
 * It provides a method to execute a service.
 */
public class Service {

    /**
     * This method is used to execute a service.
     * In this context, it simply prints a message to the console.
     */
    public void executeService() {
        System.out.println("Executing service...");
    }
}