package src.main.java.CalculadoraFacade.operaciones;

/**
 * This class represents a Division operation.
 * It provides a method for dividing one integer by another.
 */
public class Division {

    /**
     * Divides the first integer by the second integer.
     *
     * @param a the first integer
     * @param b the second integer
     * @return the quotient of the two integers
     */
    public int dividir(int a, int b) {
        return a / b;
    }

}