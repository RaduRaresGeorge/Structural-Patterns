/**
 * The MPEG4CompressionCodec class is part of the complex media library.
 * It represents a MPEG4 codec, which is a specific type of codec used for encoding and decoding MPEG4 video files.
 * This class implements the Codec interface.
 */
public class MPEG4CompressionCodec implements Codec {
    /**
     * The type of this codec. For an instance of this class, the type is always "mp4".
     */
    public String type = "mp4";
}
