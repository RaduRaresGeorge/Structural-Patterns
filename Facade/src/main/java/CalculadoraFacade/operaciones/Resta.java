package src.main.java.CalculadoraFacade.operaciones;

/**
 * This class represents a Subtraction operation.
 * It provides a method for subtracting one integer from another.
 */
public class Resta {

    /**
     * Subtracts the second integer from the first integer.
     *
     * @param a the first integer
     * @param b the second integer
     * @return the difference of the two integers
     */
    public int restar(int a, int b) {
        return a - b;
    }

}